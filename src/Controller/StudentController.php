    public function index(): Response
    {
        return $this->render('student/index.html.twig', [
            'controller_name' => 'StudentController',
        ]);
    }

    #[Route('/show', name: 'show')]
    public function show(): Response
    {
        return new Response('<h1>Bonjour Abdennour </h1>');
    }
}
